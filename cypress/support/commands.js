import loc from './locators'

Cypress.Commands.add('login',(email,password) => {
  cy.get(loc.LOGIN.EMAIL).type(email)
  cy.get(loc.LOGIN.PASSWORD).type(password)
  cy.xpath(loc.LOGIN.XP_BTN_ENTRAR).click()
})
